package hw8;

public class Male {
    private int height;
    private String eyesColor;

    public Male (int height, String eyesColor) {
        this.height = height;
        this.eyesColor = eyesColor;
    }

    public int getHeight() {
        return height;
    }

    public String getEyesColor() {
        return eyesColor;
    }

}
